    def licenses(self, df):
        ls = []
        for item in df['EDD:licenses']:
            if item == {'Galactic'}:
                ls.append('License3')
            elif item == {'Sol'}:
                ls.append('License1')
            elif item == {'Interstellar'}:
                ls.append('License2')
            else:
                ls.append('No License')
        df['EDD:licenses'] = ls
        return df

